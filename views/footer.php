    <script type="text/javascript">
        
        $(document).ready(function(){
            $('.carousel').carousel();
            $(".errorAlert").hide();
            $('.btnLogin').click(function(){
                $('.btnLogin').attr('disabled','disabled');
                var username = $('#loginU').val();
                var password = $('#loginP').val();
                if (username == '' | password == '') {
                    $('.btnLogin').removeAttr("disabled");
                    $("#loginModal .errorAlert p").html('Lỗi : Tài khoản hoặc mật khẩu không đúng ');
                    $("#loginModal .errorAlert").slideDown();
                    setTimeout(function(){
                        $("#loginModal .errorAlert").slideUp();
                    },5000);
                }else{
                    $.ajax({
                    type: 'POST',
                    url: 'include/loginProcess.php',
                    data : 'username=' + username + "&password=" + password,
                    success: function(res){
                        if (res == 'done') {
                            $('#loginModal .modal-body').html('Đăng nhập thành công ! trình duyệt sẽ tự động chuyển trong 3 giây');
                            setTimeout(function(){
                                location.href='admincp/index.php';
                            },5000);
                            
                        }else{
                            $('.btnLogin').removeAttr("disabled");
                            $("#loginModal .errorAlert p").html('Lỗi : vui lòng kiểm tra lại tài khoản hoặc mật khẩu');
                            $("#loginModal .errorAlert").slideDown();
                            setTimeout(function(){
                                $("#loginModal .errorAlert").slideUp();
                            },5000);
                        }
                    }
                });// END ajax
                }
            }); // end btnLogin
            
            $('.btnRegister').click(function(){
                var firstname = $('#firstname').val();
                var lastname = $('#lastname').val();
                var address = $('#address').val();
                var email = $('#email').val();
                var phone = $('#phone').val();
                var birthday = $('#birthday').val();
                var sex = $('#sex').val();
                var username = $('#username').val();
                var password = $('#password').val();
                
                $.ajax({
                    type: 'POST',
                    url: 'include/registerProcess.php',
                    data : 'username=' + username + "&password=" + password + "&firstname=" + firstname + "&lastname=" + lastname + "&address=" + address + "&email=" + email + "&phone=" + phone + "&birthday=" + birthday + "&sex=" + sex,
                    success: function(res){
                        
                        if (res == 'done') {
                            $('#registerModal .modal-body').html('Đăng ký thành công ! , vui long nhấn vào đây để đăng nhập');
                        }else{
                            
                            $("#registerModal .errorAlert p").html('Lỗi : vui lòng kiểm tra lại tài khoản hoặc mật khẩu');
                            $("#registerModal .errorAlert").slideDown();
                            setTimeout(function(){
                                $("#registerModal .errorAlert").slideUp();
                            },5000);
                        }
                    }
                })
            }); // end btnLogin
            
            $('.btnPreview').click(function(){
                var productId = $(this).attr('data-id');
                var productname = $(this).attr('data-name');
                var productprice = $(this).attr('data-price');
                var productImg = $(this).attr('data-img');
                $('#myModal #myModalLabel').html(productname);
                $('#myModal .price').html(productprice);
                $('#myModal .size').html('XL');
                $('#myModal .hiddenId').val(productId);
                $('.modalImg').attr('src','uploads/product/' + productImg);
            });
            
        });
